let day = 1;

switch (day) {
    case 1:
        console.log(`It is monday!`);

        break;
    case 2:
        console.log(`Tusday`);

        break;
    case 3:
        console.log(`Wed`);

        break;
    case 4:
        console.log(`Thurs`);

        break;
    case 5:
        console.log(`Fri`);

        break;
    case 6:
        console.log(`Sat`);

        break;
    case 7:
        console.log(`Sun`);

        break;
    default:
        console.log(`${day} is not a day!`);

        break;
}